class ItemTile extends StatelessWidget {
  final Food food;

  const ItemTile(this.food);

  @override
  Widget build(BuildContext context) {
    var favoritesList = Provider.of<Favorites>(context);

    return Padding(
      padding: const EdgeInsets.all(8.0),
      child: ListTile(
        leading: CircleAvatar(
          backgroundColor: Colors.primaries[food.cid % Colors.primaries.length],
          child: Text(
            food.cid.toString(),
            key: Key('text_$food.cid'),
          ),
        ),
        title: Text(
          food.category,
          key: Key('text_$food.category'),
        ),
        trailing: IconButton(
          key: Key('icon_$food.category'),
          icon: favoritesList.items.contains(food)
              ? Icon(Icons.favorite)
              : Icon(Icons.favorite_border),
          onPressed: () {
            print(favoritesList.items.contains(food));
            !favoritesList.items.contains(food)
                ? favoritesList.add(food)
                : favoritesList.remove(food);
            ScaffoldMessenger.of(context).showSnackBar(
              SnackBar(
                content: Text(favoritesList.items.contains(food)
                    ? 'Added to favorites.'
                    : 'Removed from favorites.'),
                duration: Duration(seconds: 1),
              ),
            );
          },
        ),
      ),
    );
  }
}
